def get_translation(key: str, language: str = "English") -> str:
    """
    Get translation for a key in the specified language
    
    Args:
        key: Translation key
        language: Language code (English, French, Portuguese, Spanish)
    
    Returns:
        Translated string or key if translation not found
    """
    if language not in TRANSLATIONS:
        language = "English"
    
    return TRANSLATIONS.get(language, {}).get(key, key)


def translate(text: str, language: str = "English") -> str:
    """
    Translate text if translation exists, otherwise return original
    
    Args:
        text: Text to translate
        language: Language code
    
    Returns:
        Translated text or original if no translation
    """
    # Try to find translation key
    for lang_dict in TRANSLATIONS.values():
        for key, value in lang_dict.items():
            if value == text:
                return get_translation(key, language)
    
    return text

